import { usePermissions } from '../../context/PermissionContext';
import './RoleBadge.css';

/**
 * RoleBadge Component
 * 
 * Displays user's role with appropriate styling and icons
 */

const RoleBadge = ({ className = '', size = 'normal' }) => {
  const { getRoleDisplayName, getRoleBadgeColor, getUserRole, isTrustedMember } = usePermissions();

  const role = getUserRole();
  const displayName = getRoleDisplayName();
  const color = getRoleBadgeColor();
  const trusted = isTrustedMember();

  const getRoleIcon = () => {
    switch (role) {
      case 'admin':
        return (
          <svg width="14" height="14" fill="currentColor" viewBox="0 0 20 20">
            <path fillRule="evenodd" d="M11.49 3.17c-.38-1.56-2.6-1.56-2.98 0a1.532 1.532 0 01-2.286.948c-1.372-.836-2.942.734-2.106 2.106.54.886.061 2.042-.947 2.287-1.561.379-1.561 2.6 0 2.978a1.532 1.532 0 01.947 2.287c-.836 1.372.734 2.942 2.106 2.106a1.532 1.532 0 012.287.947c.379 1.561 2.6 1.561 2.978 0a1.533 1.533 0 012.287-.947c1.372.836 2.942-.734 2.106-2.106a1.533 1.533 0 01.947-2.287c1.561-.379 1.561-2.6 0-2.978a1.532 1.532 0 01-.947-2.287c.836-1.372-.734-2.942-2.106-2.106a1.532 1.532 0 01-2.287-.947zM10 13a3 3 0 100-6 3 3 0 000 6z" clipRule="evenodd" />
          </svg>
        );
      case 'member':
        return (
          <svg width="14" height="14" fill="currentColor" viewBox="0 0 20 20">
            <path d="M9 12l2 2 4-4m6 2a9 9 0 11-18 0 9 9 0 0118 0z" />
          </svg>
        );
      case 'viewer':
        return (
          <svg width="14" height="14" fill="currentColor" viewBox="0 0 20 20">
            <path d="M10 12a2 2 0 100-4 2 2 0 000 4z" />
            <path fillRule="evenodd" d="M.458 10C1.732 5.943 5.522 3 10 3s8.268 2.943 9.542 7c-1.274 4.057-5.064 7-9.542 7S1.732 14.057.458 10zM14 10a4 4 0 11-8 0 4 4 0 018 0z" clipRule="evenodd" />
          </svg>
        );
      default:
        return null;
    }
  };

  return (
    <span 
      className={`role-badge role-badge--${role} role-badge--${size} ${trusted ? 'role-badge--trusted' : ''} ${className}`}
    >
      <span className="role-badge__icon">
        {getRoleIcon()}
      </span>
      <span className="role-badge__text">
        {displayName}
      </span>
      {trusted && (
        <span className="role-badge__crown">
          <svg width="12" height="12" fill="currentColor" viewBox="0 0 20 20">
            <path d="M9.049 2.927c.3-.921 1.603-.921 1.902 0l1.07 3.292a1 1 0 00.95.69h3.462c.969 0 1.371 1.24.588 1.81l-2.8 2.034a1 1 0 00-.364 1.118l1.07 3.292c.3.921-.755 1.688-1.54 1.118l-2.8-2.034a1 1 0 00-1.175 0l-2.8 2.034c-.784.57-1.838-.197-1.539-1.118l1.07-3.292a1 1 0 00-.364-1.118L2.98 8.72c-.783-.57-.38-1.81.588-1.81h3.461a1 1 0 00.951-.69l1.07-3.292z" />
          </svg>
        </span>
      )}
    </span>
  );
};

export default RoleBadge;